# Dictionary is a collection of keys-value pairs.
marks = {
    "Meeran"    : 100,
    "Pankaj"    : 90,
    "Faiz"      : 80,
    "Rohan"     : 60,
    "List"      : [1,2,3]
}

print(marks, type(marks))
print(marks["Meeran"])
print(marks["List"], type(marks["List"]))

''' 
PROPERTIES OF PYTHON DICTIONARIES
 1. It is unordered.
 2. It is mutable. 
 3. It is indexed. 
 4. Cannot contain duplicate keys
'''
